package me.seby.pew.util;

public class Location {
	private int x;
	private int y;
	
	public Location(int x1,int y1){
		x=x1;
		y=y1;
	}

	public int getX() {
		return x;
	}

	public void setX(int x1) {
		this.x = x1;
	}

	public int getY() {
		return y;
	}

	public void setY(int y1) {
		this.y = y1;
	}
	

}
